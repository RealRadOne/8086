ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUMA DB 05H
DATA ENDS
CODE SEGMENT
START:
     MOV AX,DATA
     MOV DS,AX
     LEA SI,NUMA
     MOV BX,[SI]
     CALL FACT
     MOV BX,AX
     HLT

FACT PROC NEAR
     MOV CX,BX
     MOV AX,01H
L1  :MUL CX
     DEC CX
     CMP CX,01H
     JAE L1
     RET
     FACT ENDP
     
CODE ENDS
END START
